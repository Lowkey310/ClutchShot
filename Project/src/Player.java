
public class Player
	{
		 private String name;
		 private int percentage;
		 
		 public Player ( String n, int p )
		 {
			 name = n;
			 percentage = p;
		 }

		public String getName()
			{
				return name;
			}

		public void setName(String name)
			{
				this.name = name;
			}

		public int getPercentage()
			{
				return percentage;
			}

		public void setPercentage(int percentage)
			{
				this.percentage = percentage;
			}

		
		
	}
